package com.example.semis.stressapp;

public class AccVector {
    private double x, y, z;
    AccVector(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }


    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}
